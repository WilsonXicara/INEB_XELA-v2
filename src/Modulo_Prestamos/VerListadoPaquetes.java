    public VerListadoPaquetes(java.awt.Frame parent, Connection conexion) {
        super(parent, true);
        initComponents();
        this.conexion = conexion;
        hacerVisible = true;
        
        // Obtengo la información de todos los Paquetes de libros guardados en la Base de Datos
        try {
            ResultSet cConsulta = conexion.createStatement(ResultSet.TYPE_FORWARD_ONLY, ResultSet.CONCUR_READ_ONLY)
                    .executeQuery("SELECT PaqueteLibro.*, COUNT(Libro.PaqueteLibro_Id) cantidadLibros FROM PaqueteLibro "
                    + "INNER JOIN Libro ON PaqueteLibro.Id = Libro.PaqueteLibro_Id "
                    + "WHERE Libro.Estado != '6' "
                    + "GROUP BY PaqueteLibro_Id");
            listaIDPaquetes = new ArrayList<>();
            int contador = 0;
            DefaultTableModel modelPaquetes = (DefaultTableModel)tabla_paquetes.getModel();
            while(cConsulta.next()) {
                listaIDPaquetes.add(cConsulta.getInt("Id"));
                modelPaquetes.addRow(new String[]{
                    ""+(++contador),
                    cConsulta.getString("Codigo"),
                    cConsulta.getString("Descripcion")==null ? "<Sin especificar>" : cConsulta.getString("Descripcion"),
                    ""+cConsulta.getInt("cantidadLibros")
                });
            }
            // Otras configuraciones importantes
            filtroTabla = new TableRowSorter(tabla_paquetes.getModel()); // Objeto que permite filtrar filas de la Tabla
            tabla_paquetes.setShowHorizontalLines(true);  // Para mostrar los bordes de las celdas de la tabla
            tabla_paquetes.setShowVerticalLines(true);
            tabla_detalle_libros.setShowHorizontalLines(true);  // Para mostrar los bordes de las celdas de la tabla
            tabla_detalle_libros.setShowVerticalLines(true);
            panel_descripcion_paquete.setVisible(false);
            panel_nuevo_libro.setVisible(false);
            this.setLocationRelativeTo(null);   // Para centrar esta ventana sobre la pantalla
        } catch (SQLException ex) {
            hacerVisible = false;
            JOptionPane.showMessageDialog(this, "No se puede extraer alguno de los registros.\n\nDescripción:\n"+ex.getMessage(), "Error en conexión", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(VerListadoPaquetes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

