    private void guardar_descripcion_de_paqueteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardar_descripcion_de_paqueteActionPerformed
        String descripcion = descripcion_paquete.getText();
        try {
            // Actualización del registro en la Base de Datos y en la Tabla
            conexion.prepareStatement("UPDATE PaqueteLibro SET"
                    + " Descripcion = "+(descripcion.length()==0 ? "NULL" : "'"+descripcion+"'")+" WHERE Id = "+listaIDPaquetes.get(indexPaqueteEditado)).executeUpdate();
            ((DefaultTableModel)tabla_paquetes.getModel()).setValueAt(descripcion.length()==0 ? "<Sin especificar>" : descripcion, indexPaqueteEditado, 2);
            codigo_paquete.setText("");     // Limpieza de campos
            descripcion_paquete.setText("");
            panel_descripcion_paquete.setVisible(false);
            tabla_paquetes.setEnabled(true);   // Habilitación de los componentes de visualización de Paquetes
            tabla_paquetes.removeRowSelectionInterval(0, tabla_paquetes.getRowCount()-1);
            obtener_libros_de_paquete(-1);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "No se puede actualizar los datos del Paquete.\n\nDescripción:\n"+ex.getMessage(), "Error en conexión", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(VerListadoPaquetes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_guardar_descripcion_de_paqueteActionPerformed

