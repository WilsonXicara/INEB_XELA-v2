    private void obtener_libros_de_paquete(int indexPaquete) {
        if (indexPaquete == -1)   // Borrar campos
            ((DefaultTableModel)tabla_detalle_libros.getModel()).setRowCount(0);
        else {
            try {
                Statement sentencia = conexion.createStatement(ResultSet.TYPE_FORWARD_ONLY, ResultSet.CONCUR_READ_ONLY);
                ResultSet cConsulta = sentencia.executeQuery("SELECT Codigo, Nombre, Estado FROM Libro "
                        + "WHERE PaqueteLibro_Id = "+listaIDPaquetes.get(indexPaquete)+" AND Estado != '6' "
                        + "ORDER BY Nombre");
                DefaultTableModel modelLibros = (DefaultTableModel)tabla_detalle_libros.getModel();
                ArrayList<String> estados = new ArrayList<>();
                int contador = estado_libro.getItemCount();
                for(int i=0; i<contador; i++)
                    estados.add(estado_libro.getItemAt(i));
                contador = 0;
                while(cConsulta.next())
                    modelLibros.addRow(new String[]{
                        ""+(++contador),
                        cConsulta.getString("Codigo"),
                        cConsulta.getString("Nombre"),
                        estados.get(cConsulta.getInt("Estado")-1)
                    });
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "No se puede extraer alguno de los registros de Libros.\n\nDescripción:\n"+ex.getMessage(), "Error en conexión", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(VerListadoPaquetes.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    private void validar_datos_libro() throws ExcepcionDatosIncorrectos {
        // Todos los Libros que se muestran se obtienen de la Base de Datos (no hay temporales)
        String codigoLibro = codigo_libro.getText();
        if (codigoLibro.length() == 0)
            throw new ExcepcionDatosIncorrectos("No ha especificado el Código del Libro");
        if (codigoLibro.length() != 5)
            throw new ExcepcionDatosIncorrectos("El Código del Libro debe tener 5 caracteres");
        if (nombre_libro.getText().length() == 0)
            throw new ExcepcionDatosIncorrectos("No ha especificado el Nombre del Libro");
        // Los otros campos no son obligatorios
        try {   // Verificación de que no exista otro Libro con el mismo Código en la Base de Datos
            ResultSet cConsulta = conexion.createStatement(ResultSet.TYPE_FORWARD_ONLY, ResultSet.CONCUR_READ_ONLY)
                    .executeQuery("SELECT Nombre, Autor, Editorial FROM Libro WHERE Codigo = '"+codigoLibro+"'");
            if (cConsulta.next())
                throw new ExcepcionDatosIncorrectos("Ya se registró un Libro con el mismo Código:"
                        + "\nCódigo:     "+codigoLibro
                        + "\nNombre:     "+cConsulta.getString("Nombre")
                        + "\nAutor:      "+(cConsulta.getString("Autor")==null ? "<No especificado>" : cConsulta.getString("Autor"))
                        + "\nEditorial:  "+(cConsulta.getString("Editorial")==null ? "<No especificado>" : cConsulta.getString("Editorial"))
                        + "\n\nNo se puede agregar el nuevo libro.");
        } catch (SQLException ex) { // Si ocurre un error, se asume que posiblemente ya exista el libro
            Logger.getLogger(VerListadoPaquetes.class.getName()).log(Level.SEVERE, null, ex);
            throw new ExcepcionDatosIncorrectos("Error al comprobar existencia del Libro.\n\nDescripción:\n"+ex.getMessage());
        }
    }
    private void limpiar_campos_libro() {
        codigo_libro.setText("");
        nombre_libro.setText("");
        autor_libro.setText("");
        editorial_libro.setText("");
        estado_libro.setSelectedIndex(0);
    }
