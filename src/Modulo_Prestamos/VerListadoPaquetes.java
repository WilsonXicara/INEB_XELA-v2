    private void agregar_libroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregar_libroActionPerformed
        // Previo a agregar un libro, verifico que no exista otro en la tabla o en la Base de Datos con el mismo Código
        try {
            validar_datos_libro();  // Comprobación de que los datos sean correctos y que no se repitan
            // Creación del registro del nuevo Libro en la Base de Datos
            conexion.prepareStatement("INSERT INTO Libro(Codigo, Nombre, Autor, Editorial, Estado, PaqueteLibro_Id) VALUES("
                    + "'"+codigo_libro.getText()+"', "
                    + "'"+nombre_libro.getText()+"', "
                    + (autor_libro.getText().length()==0 ? "NULL" : "'"+autor_libro.getText()+"'")+", "
                    + (editorial_libro.getText().length()==0 ? "NULL" : "'"+editorial_libro.getText()+"'")+", "
                    + "'"+(estado_libro.getSelectedIndex()+1)+"', "
                    + listaIDPaquetes.get(indexPaqueteEditado)+")").executeUpdate();
            // Aumento el contador de libros del paquete seleccionado
            DefaultTableModel modelPaquetes = (DefaultTableModel)tabla_paquetes.getModel();
            modelPaquetes.setValueAt(""+(Integer.parseInt(modelPaquetes.getValueAt(indexPaqueteEditado, 3).toString())+1), indexPaqueteEditado, 3);
            JOptionPane.showMessageDialog(this, "El Libro fue agregado al Paquete No. "+(indexPaqueteEditado+1)+" con éxito", "Agregar libro", JOptionPane.INFORMATION_MESSAGE);
            limpiar_campos_libro();     // Limpio los componentes de entrada de datos, y de muestra de datos
            obtener_libros_de_paquete(-1);
            panel_nuevo_libro.setVisible(false);
            tabla_paquetes.setEnabled(false);   // Habilito los componentes para permitir la selección de otro Paquete
            tabla_paquetes.removeRowSelectionInterval(0, tabla_paquetes.getRowCount()-1);
        } catch (ExcepcionDatosIncorrectos ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error en datos", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(CrearPaqueteLibro.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "No se puede crear el registro del nuevo Libro.\n\nDescripción:\n"+ex.getMessage(), "Error en conexión", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(VerListadoPaquetes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_agregar_libroActionPerformed

